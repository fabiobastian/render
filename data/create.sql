CREATE TABLE VS_15_EQUIPE_2.CLIENTE
(
    id_cliente NUMBER NOT NULL,
    nome       VARCHAR2(200) NOT NULL,
    email      VARCHAR2(200) NOT NULL,
    telefone   VARCHAR2(15) NOT NULL,
    PRIMARY KEY (id_cliente)
);

CREATE TABLE VS_15_EQUIPE_2.ENDERECO
(
    id_endereco NUMBER NOT NULL,
    id_cliente  NUMBER NOT NULL,
    logradouro  VARCHAR2(50) NOT NULL,
    numero      VARCHAR2(10) NOT NULL,
    bairro      VARCHAR2(50) NOT NULL,
    cidade      VARCHAR2(50) NOT NULL,
    cep         VARCHAR2(10) NOT NULL,
    PRIMARY KEY (id_endereco),
    CONSTRAINT fk_endereco_cliente FOREIGN KEY (id_cliente)
        REFERENCES VS_15_EQUIPE_2.CLIENTE (id_cliente)
);

CREATE TABLE VS_15_EQUIPE_2.PRODUTO
(
    id_produto NUMBER NOT NULL,
    nome       VARCHAR2(50) NOT NULL,
    descricao  VARCHAR2(100) NOT NULL,
    preco      NUMBER NOT NULL,
    tamanho    NUMBER NOT NULL,
    cor        VARCHAR2(10) NOT NULL,
    estoque    NUMBER NOT NULL,
    PRIMARY KEY (id_produto)
);

CREATE TABLE VS_15_EQUIPE_2.PEDIDO
(
    id_pedido     NUMBER NOT NULL,
    id_cliente    NUMBER NOT NULL,
    data          DATE   NOT NULL,
    status_pedido VARCHAR2(45) NOT NULL,
    PRIMARY KEY (id_pedido),
    CONSTRAINT fk_pedido_cliente FOREIGN KEY (id_cliente)
        REFERENCES VS_15_EQUIPE_2.CLIENTE (id_cliente)
);

CREATE TABLE VS_15_EQUIPE_2.ITEM_PEDIDO
(
    id_item_pedido NUMBER NOT NULL,
    id_pedido       NUMBER NOT NULL,
    id_produto      NUMBER NOT NULL,
    quantidade      NUMBER NOT NULL,
    PRIMARY KEY (id_item_pedido),
    CONSTRAINT fk_item_pedido_pedido FOREIGN KEY (id_pedido)
        REFERENCES VS_15_EQUIPE_2.PEDIDO (id_pedido),
    CONSTRAINT fk_item_pedido_produto FOREIGN KEY (id_produto)
        REFERENCES VS_15_EQUIPE_2.PRODUTO (id_produto)
);

CREATE TABLE VS_15_EQUIPE_2.PAGAMENTO
(
    id_pagamento    NUMBER NOT NULL,
    id_pedido       NUMBER NOT NULL,
    valor           NUMBER NOT NULL,
    forma_pagamento VARCHAR2(45) NOT NULL,
    PRIMARY KEY (id_pagamento),
    CONSTRAINT fk_pagamento_pedido FOREIGN KEY (id_pedido)
        REFERENCES VS_15_EQUIPE_2.PEDIDO (id_pedido)
);



